    public static boolean isNumber(String str) {
        try {
            Integer.parseInt(str);
            return true;
        } catch (NumberFormatException e) {
            return false;
        }
    }

    public boolean validate(String ac_num, String branch_id, String balance) {
        if ((isNumber(ac_num) && isNumber(branch_id) && isNumber(balance)) == false) {
            JOptionPane.showMessageDialog(this, "Data must be a number");
            return true;
        } else if (ac_num.equals("") || branch_id.equals("") || balance.equals("")) {
            JOptionPane.showMessageDialog(this, "Please fill all the infomation");
            return false;
        }
        return true;
    }

    private void add_btnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_add_btnMouseClicked
        String account_num, branch_id, balance;
        account_num = txtAccount_num.getText();
        branch_id = txtBranch.getText();
        balance = txtBalance.getText();
        
        if(validate(account_num, branch_id, balance)){
            try {
                pst = con.prepareStatement("insert into current_account (account_number, balance, branch_id) values (?,?,?)");
                pst.setString(1, account_num);
                pst.setString(2, balance);
                pst.setString(3, branch_id);
                pst.executeUpdate();
                
                JOptionPane.showMessageDialog(this, "Successfully");
                showCurrentAccount();
                txtAccount_num.setText("");
                txtBalance.setText("");
                txtBranch.setText("");
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Account number already existed OR Branch ID doesn't exist");
            }
        }
    }//GEN-LAST:event_add_btnMouseClicked

