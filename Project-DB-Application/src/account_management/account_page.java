    private void delete_btnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_delete_btnMouseClicked
        int index = current_account_tb.getSelectedRow();
        if (index < 0) {
            JOptionPane.showMessageDialog(this, "Choose a row you want to delete first");
        } else {
            TableModel model = current_account_tb.getModel();
            String accountNum = (String) model.getValueAt(index, 0);
            try {
                pst = con.prepareStatement("delete from current_account where account_number = '" + accountNum + "'");
                int dialogButton = JOptionPane.showConfirmDialog(null, "Are you sure?", "WARNING", JOptionPane.YES_NO_OPTION);

                if (dialogButton == JOptionPane.YES_OPTION) {
                    pst.executeUpdate();
                    showCurrentAccount();
                }
            } catch (SQLException ex) {
                Logger.getLogger(account_page.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_delete_btnMouseClicked

